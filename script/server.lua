local resource = {}
resource["functions"] = {}

resource["functions"]["ant_bug"] = function (res)
    for _, glitch in ipairs(config["home"]["bugs"]) do
        setGlitchEnabled(glitch, false)
     end 
end
addEventHandler("onResourceStart", resourceRoot, resource["functions"]["ant_bug"])

